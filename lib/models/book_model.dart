class BookModel {
  final String id;
  final String title;
  final String author;
  final String description;
  final String image;
  final String category;
  final String pages;
 final String publishYear;
  

  BookModel({
    required this.id,
    required this.title,
    required this.author,
    required this.description,
    required this.image,
    required this.category,
    required this.pages,
    required this.publishYear,
  });
}
